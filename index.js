function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(n) {
  var a = parseInt('n', 10)
  var b = parseInt('0x2328')
  var c = parseInt('sldkjflksjf')

  var n = a + b + c

  return n
}
